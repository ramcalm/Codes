class Solution
{
    public:
    Node* addOne(Node *head) 
    {
        Node *newhead=new Node(0);
        newhead->next=head;
        stack<Node*> stk;
        stk.push(newhead);
        while(stk.top()->next)
            stk.push(stk.top()->next);
        int cy=1;
        while(!stk.empty())
        {
            stk.top()->data+=cy;
            cy=stk.top()->data/10;
            stk.top()->data%=10;
            stk.pop();
        }
        if(newhead->data) return newhead;
        return head;
    }
};

